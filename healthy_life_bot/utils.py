def calculate_water_norm(weight: float, height: float) -> float:
    """–†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ—Ç —Ä–µ–∫–æ–º–µ–Ω–¥—É–µ–º—É—é –¥–Ω–µ–≤–Ω—É—é –Ω–æ—Ä–º—É –ø–æ—Ç—Ä–µ–±–ª–µ–Ω–∏—è –≤–æ–¥—ã.

    –§–æ—Ä–º—É–ª–∞ —Ä–∞—Å—á–µ—Ç–∞: (–≤–µ—Å * 0.03 + —Ä–æ—Å—Ç * 0.01) –ª–∏—Ç—Ä–æ–≤

    Args:
        weight: –í–µ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ –∫–∏–ª–æ–≥—Ä–∞–º–º–∞—Ö
        height: –†–æ—Å—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ —Å–∞–Ω—Ç–∏–º–µ—Ç—Ä–∞—Ö

    Returns:
        float: –†–µ–∫–æ–º–µ–Ω–¥—É–µ–º–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–æ–¥—ã –≤ –ª–∏—Ç—Ä–∞—Ö, –æ–∫—Ä—É–≥–ª–µ–Ω–Ω–æ–µ –¥–æ 1 –∑–Ω–∞–∫–∞
    """

    return round((weight * 0.03 + height * 0.01), 1)
